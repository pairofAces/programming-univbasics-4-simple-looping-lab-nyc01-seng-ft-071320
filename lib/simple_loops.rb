def loop_message_five_times(message)
   message = "Hello World."
   counter = 0
   while counter <= 5 do
     puts message
     counter += 1
   end
 end

def loop_message_n_times(message, number)
  number = 0
  message = "Hello Moon."
  while number <= 5 do
    puts message
    number += 1
  end
end


  
  
  